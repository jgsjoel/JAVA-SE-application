    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:

        try {

            ResultSet rs = Mysql.search("SELECT `supplier`.`id`,`supplier`.`fname`,`supplier`.`lname`,`company`.`name` AS `company`,`city`.`name` AS `city`\n"
                    + " FROM `supplier` INNER JOIN `company` ON `company`.`id`=`supplier`.`company_id` INNER JOIN `company_branch`\n"
                    + "  ON `company_branch`.`id`=`supplier`.`company_branch_id` INNER JOIN `city`\n"
                    + "  ON `city`.`id`=`company_branch`.`city_id` WHERE `supplier`.`id`='" + sup_id + "' ");

            rs.next();

            InputStream stream = GRNbasic.class.getResourceAsStream("/reports/GRNSupplierItemHistory.jrxml");

            JasperReport jr = JasperCompileManager.compileReport(stream);

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

            JRTableModelDataSource dataSource = new JRTableModelDataSource(dtm);

            HashMap parameters = new HashMap();
            parameters.put("id", rs.getString("id"));
            parameters.put("name", rs.getString("fname") + " " + rs.getString("lname"));
            parameters.put("company", rs.getString("company"));
            parameters.put("city", rs.getString("city"));

            String date_range;
            if (from_date != null && to_date != null) {
                date_range = sdf.format(from_date) + " TO " + sdf.format(to_date);
            } else if (from_date != null && to_date == null) {
                date_range = "From " + sdf.format(from_date);
            } else if (from_date == null && to_date != null) {
                date_range = "TO " + sdf.format(from_date);
            } else {
                date_range = "N/S";
            }

            parameters.put("range", date_range);

            JasperPrint jp = JasperFillManager.fillReport(jr, parameters, dataSource);

            JasperViewer.viewReport(jp, false);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton3ActionPerformed


