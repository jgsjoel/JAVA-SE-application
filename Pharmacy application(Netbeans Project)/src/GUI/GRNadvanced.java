    public void loadTable() {
        try {

            ResultSet rs = Mysql.search("SELECT `grn`.`id` AS `id`,`product`.`name` AS `name`,`grn_item`.`qty` AS `qty`,`grn_item`.`buying_price` AS `buying_price`,\n"
                    + "(`grn_item`.`qty`) *(`grn_item`.`buying_price`) AS `total`,`grn`.`date_time` AS `date_time`\n"
                    + " FROM `grn` INNER JOIN grn_item ON grn_item.grn_id=grn.id INNER JOIN stock ON grn_item.stock_id=stock.id\n"
                    + "INNER JOIN product ON product.id=stock.product_id");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("name"));
                v.add(rs.getString("qty"));
                v.add(rs.getString("buying_price"));
                v.add(rs.getString("total"));
                v.add(rs.getString("date_time"));
                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

