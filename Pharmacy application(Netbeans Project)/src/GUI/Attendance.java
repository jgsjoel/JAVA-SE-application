    public void loadAttendance() {

        try {

            ResultSet rs = Mysql.search("SELECT `employee`.`id`,`employee`.`fname`,`employee`.`lname`,`employee`.`nic`,`attendance`.`date`,\n"
                    + "`attendance`.`arrived_time`,`attendance`.`departure_time`,`attend_status`.`name` FROM `attendance`\n"
                    + " INNER JOIN `employee` ON `employee`.`id`=`attendance`.`employee_id` INNER JOIN\n"
                    + "  `attend_status` ON `attend_status`.`id`=`attendance`.`attend_status_id`");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("fname") + " " + rs.getString("lname"));
                v.add(rs.getString("nic"));
                v.add(rs.getString("date"));
                v.add(rs.getString("arrived_time"));
                v.add(rs.getString("departure_time"));
                v.add(rs.getString("name"));
                dtm.addRow(v);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

