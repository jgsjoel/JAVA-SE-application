    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String id = jTextField1.getText();
        String name = jTextField2.getText();
        String nic = jTextField3.getText();
        String date = jTextField4.getText();
        String time = sdf2.format(new Date());

        if (id.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please select an employee", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {
                ResultSet rs = Mysql.search("SELECT * FROM `attendance` WHERE `date`='" + date + "' AND employee_id='" + id + "'");

                if (rs.next()) {
                    JOptionPane.showMessageDialog(this, "Attendance of this employe has already been added", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {

                    if (jCheckBox2.isSelected()) {
                        Mysql.iud("INSERT INTO `attendance`(`employee_id`,`date`,`arrived_time`,`departure_time`,`attend_status_id`) VALUES('" + id + "','" + date + "','00:00:00','00:00:00','2')");
                    } else {
                        Mysql.iud("INSERT INTO `attendance`(`employee_id`,`date`,`arrived_time`) VALUES('" + id + "','" + date + "','" + time + "')");
                    }
                    JOptionPane.showMessageDialog(this, "Attendance Added!", "Success", JOptionPane.INFORMATION_MESSAGE);
                    clearFields();
                }

            } catch (Exception e) {
                e.printStackTrace();
            }

        }


    }//GEN-LAST:event_jButton1ActionPerformed

