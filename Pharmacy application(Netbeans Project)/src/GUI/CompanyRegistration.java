    public void loadCompanyBranches(String query) {

        try {
            ResultSet rs = Mysql.search("SELECT `company`.`id` AS `company_id`,`company_branch`.`id`,`company`.`name` AS `company_name`, `company_branch`.`address_line1` AS `address1`,`company_branch`.`address_line2` AS `address2`, `company_branch`.`contact_no` AS `contact`,`city`.`name` AS `city_name` FROM company INNER JOIN company_branch ON company.id=company_branch.company_id INNER JOIN city ON company_branch.city_id=city.id " + query + "");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("company_id"));
                v.add(rs.getString("company_name"));
                v.add(rs.getString("address1"));
                v.add(rs.getString("address2"));
                v.add(rs.getString("contact"));
                v.add(rs.getString("city_name"));
                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    public void loadCities() {
        try {

            ResultSet rs = Mysql.search("SELECT * FROM `city`");

            Vector v = new Vector();
            v.add("Select City");
            while (rs.next()) {
                v.add(rs.getString("name"));
            }
            DefaultComboBoxModel dcm = new DefaultComboBoxModel(v);
            jComboBox1.setModel(dcm);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

