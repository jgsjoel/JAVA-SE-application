    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:

        String id = jTextField4.getText();
        String address1 = jTextArea1.getText();
        String address2 = jTextArea2.getText();
        String contact = jTextField2.getText();
        String city = jComboBox1.getSelectedItem().toString();

        if (id.equals("N/S")) {
            JOptionPane.showMessageDialog(this, "Please select company", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (address1.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter address 01", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (address2.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter address 02", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (contact.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter contact", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (contact.length() != 10) {
            JOptionPane.showMessageDialog(this, "mobile should be 10 characters", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("07[1245678][0-9]{7}|0[12345689][12345678][0-9]{7}").matcher(contact).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid contact number format", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (city.equals("Select City")) {
            JOptionPane.showMessageDialog(this, "Please select a city", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {

                ResultSet rs1 = Mysql.search("SELECT * FROM `company_branch` WHERE `id`!='"+branch_id+"' AND `company_id`='" + id + "' AND `address_line1`='" + address1 + "' AND `address_line2`='" + address2 + "' AND `contact_no`='" + contact + "'");

                if (rs1.next()) {
                    JOptionPane.showMessageDialog(this, "This branch has already been registered to this company", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {

                    ResultSet rs2 = Mysql.search("SELECT * FROM `city` WHERE `name`='" + city + "'");
                    rs2.next();
                    String city_id = rs2.getString("id");

                    Mysql.iud("UPDATE `company_branch` SET `address_line1`='" + address1 + "',`address_line2`='" + address2 + "',`contact_no`='" + contact + "',`company_id`='" + id + "',`city_id`='" + city_id + "' WHERE id='" + branch_id + "'");
                    JOptionPane.showMessageDialog(this, "Updated Complete", "Warning", JOptionPane.WARNING_MESSAGE);
                    clearFields();
                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

