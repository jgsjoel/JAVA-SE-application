    public void loadTable() {
        try {

            ResultSet rs = Mysql.search("SELECT `grn`.`id` AS `id`,`grn_payment`.`payment_amount` AS `amount`,`grn`.`date_time` FROM grn INNER JOIN grn_payment ON grn.id=grn_payment.grn_id");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("id"));
                v.add(rs.getString("amount"));
                v.add(rs.getString("date_time"));
                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public void loadTable(String query) {
        try {

            ResultSet rs = Mysql.search("SELECT `grn`.`id` AS `id`,`grn_payment`.`payment_amount` AS `amount`,`grn`.`date_time` FROM grn INNER JOIN grn_payment ON grn.id=grn_payment.grn_id " + query + "");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            int x = 0;
            while (rs.next()) {
                x = x + 1;
                Vector v = new Vector();
                v.add("#" + x);
                v.add(rs.getString("id"));
                v.add(rs.getString("amount"));
                v.add(rs.getString("date_time"));
                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

