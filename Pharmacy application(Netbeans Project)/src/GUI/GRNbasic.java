    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select a row", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            String grn_id = jTable1.getValueAt(row, 0).toString();

            try {

                ResultSet rs = Mysql.search("SELECT `grn_payment`.`payment_amount`,`grn`.`date_time`,`supplier`.`id`,`supplier`.`fname`,`supplier`.`lname`,`company`.`name` AS `company`,`city`.`name` AS `city`\n"
                        + "   FROM `supplier` INNER JOIN `company` ON `company`.`id`=`supplier`.`company_id` INNER JOIN `company_branch`\n"
                        + "   ON `company_branch`.`id`=`supplier`.`company_branch_id` INNER JOIN `city`\n"
                        + "    ON `city`.`id`=`company_branch`.`city_id` INNER JOIN `grn` ON `grn`.`supplier_id`=`supplier`.`id` INNER JOIN `grn_payment` ON `grn_payment`.`grn_id`=`grn`.`id` WHERE `grn`.`id`='"+grn_id+"'");

                rs.next();
                
                InputStream stream = GRNbasic.class.getResourceAsStream("/reports/GRN.jrxml");

                JasperReport jr = JasperCompileManager.compileReport(stream);

                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();

                Connection dataSource = Mysql.getConnection();

                HashMap parameters = new HashMap();
                parameters.put("grn_id", grn_id);
                parameters.put("sup_id", rs.getString("id"));
                parameters.put("sup_name", rs.getString("fname")+" "+rs.getString("lname"));
                parameters.put("company", rs.getString("company"));
                parameters.put("city", rs.getString("city"));
                parameters.put("date", rs.getString("date_time"));
                parameters.put("amount", rs.getString("payment_amount"));
                        
                JasperPrint jp = JasperFillManager.fillReport(jr, parameters, dataSource);

                JasperViewer.viewReport(jp, false);

            } catch (Exception e) {
                e.printStackTrace();
            }

        }


    }//GEN-LAST:event_jButton3ActionPerformed


