    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String time = jTextField1.getText();
        String status = jComboBox1.getSelectedItem().toString();

        if (status.equals("Select Work Status")) {
            JOptionPane.showMessageDialog(this, "Please select work status", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {

            try {

                ResultSet rs1 = Mysql.search("SELECT * FROM `attend_status` WHERE `name`='" + status + "' ");
                rs1.next();
                String status_id = rs1.getString("id");

                String emp_id = att.jTable1.getValueAt(att.jTable1.getSelectedRow(), 0).toString();

                Mysql.iud("UPDATE `attendance` SET `departure_time`='" + time + "',`attend_status_id`='" + status_id + "' WHERE `employee_id`='" + emp_id + "'");
                JOptionPane.showMessageDialog(this, "Updated", "Success", JOptionPane.INFORMATION_MESSAGE);
                this.att.loadAttendance();
                this.dispose();

            } catch (Exception e) {
                e.printStackTrace();
            }

        }

    }//GEN-LAST:event_jButton1ActionPerformed

