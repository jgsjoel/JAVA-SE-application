    public void loadWorkStatus() {

        try {

            ResultSet rs = Mysql.search("SELECT * FROM `attend_status`");
            rs.next();

            Vector v = new Vector();
            v.add("Select Work Status");
            while (rs.next()) {
                v.add(rs.getString("name"));
            }
            DefaultComboBoxModel dcm = new DefaultComboBoxModel(v);
            jComboBox1.setModel(dcm);

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

