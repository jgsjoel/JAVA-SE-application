    public void loadSalaryHistory() {

        try {

            ResultSet rs = Mysql.search("SELECT `salary_history`.`year`,`salary_history`.`id` AS `sid`,`employee`.`id`,`employee`.`fname`,`employee`.`lname`,`employee`.`nic`,`salary_history`.`payment_month`,`salary_history`.`paid_salary`,`salary_history`.`paid_date_time`,`salary_history`.`year` AS `year` FROM `salary_history` INNER JOIN `employee` ON `employee`.`id`=`salary_history`.`employee_id`");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                String text = rs.getString("year");
                String t[] = text.split("-");
                
                v.add(rs.getString("sid"));
                v.add(rs.getString("id"));
                v.add(rs.getString("fname") + " " + rs.getString("lname"));
                v.add(rs.getString("nic"));
               
                Month month = Month.of(Integer.parseInt(rs.getString("payment_month")));
                
                v.add(month+" "+t[0]);
                v.add(rs.getString("paid_salary"));
                v.add(rs.getString("paid_date_time"));
                dtm.addRow(v);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

