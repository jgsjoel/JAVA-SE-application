    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        String id = jTextField1.getText();

        int m = jMonthChooser1.getMonth();
        int month = m + 1;

        int year = jYearChooser1.getYear();
        
        try {

            ResultSet rs = Mysql.search("SELECT `employee`.`id`,`employee`.`fname`,`employee`.`lname`,`employee`.`nic`,`attend_status`.`precentage`,\n"
                    + "`job_role`.`salary`,`job_role`.`name`\n"
                    + "FROM `attendance` INNER JOIN `attend_status` ON `attendance`.`attend_status_id`=`attend_status`.`id` INNER JOIN \n"
                    + "`employee` ON `employee`.`id`=`attendance`.`employee_id` INNER JOIN `job_role` ON\n"
                    + " `job_role`.`id`=`employee`.`job_role_id` WHERE `employee`.`id`='" + id + "' AND MONTH(`attendance`.`date`)=" + month + " AND YEAR(`attendance`.`date`)='"+year+"'");

            double TotalSalaryForMonth = 0;
            double SalaryDeduction = 0;

            while (rs.next()) {

                TotalSalaryForMonth = TotalSalaryForMonth + (Double.parseDouble(rs.getString("precentage")) * Double.parseDouble(rs.getString("salary"))) / 100;

            }

            if (TotalSalaryForMonth == 0) {
                JOptionPane.showMessageDialog(this, "There are no payments for the selected month and year", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {

                ResultSet rs2 = Mysql.search("SELECT * FROM `salary_history` WHERE `employee_id`='" + id + "' AND YEAR(`paid_date_time`)='" + year + "' AND `payment_month`=" + month + " ");

                while (rs2.next()) {
                    SalaryDeduction = SalaryDeduction + Double.parseDouble(rs2.getString("paid_salary"));
                }

                double SubTotal = TotalSalaryForMonth - SalaryDeduction;

                if (SubTotal == 0) {
                    JOptionPane.showMessageDialog(this, "The full payment for selected time period has already been made.", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    Mysql.iud("INSERT INTO `salary_history`(`employee_id`,`paid_date_time`,`paid_salary`,`payment_month`,`year`) VALUES('" + id + "','" + sdf.format(new Date()) + "','" + SubTotal + "','" + month + "','"+year+"')");
                    loadSalaryHistory();
                }

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int row = jTable1.getSelectedRow();
        
        if(row == -1){
            JOptionPane.showMessageDialog(this, "Plese select a payment from table", "Warning", JOptionPane.WARNING_MESSAGE);
        }else {
        
            String name = jTable1.getValueAt(row, 2).toString();
            String nic = jTable1.getValueAt(row, 3).toString();
            String month = jTable1.getValueAt(row, 4).toString();
            String amount = jTable1.getValueAt(row, 5).toString();
            String date = jTable1.getValueAt(row, 6).toString();
            
            try {
                
                InputStream stream = GRNbasic.class.getResourceAsStream("/reports/Salary_slip.jrxml");
                 
                 JasperReport jr = JasperCompileManager.compileReport(stream);
                 
                 HashMap parameters = new HashMap();
                 parameters.put("name", name);
                 parameters.put("nic", nic);
                 parameters.put("month", month);
                 parameters.put("amount", amount);
                 parameters.put("date", date);
                 
                 JREmptyDataSource dataSource = new JREmptyDataSource();
                 
                 JasperPrint jp = JasperFillManager.fillReport(jr, parameters, dataSource);
                
                 JasperViewer.viewReport(jp,false);
                 
            } catch (Exception e) {
            e.printStackTrace();
            }
            
            
        
        }
    }//GEN-LAST:event_jButton4ActionPerformed

